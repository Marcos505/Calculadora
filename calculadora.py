number_1 = int(input('Enter your first number: '))
number_2 = int(input('Enter yout second number: '))

print('{} + {} = {}' .format(number_1, number_2, (number_1 + number_2)))

#Subtraction
print('{} - {} = {}' .format(number_1, number_2, (number_1 - number_2)))

#Multiplication
print('{} * {} = {}' .format(number_1, number_2, (number_1 * number_2)))

#Division
print('{} / {} = {}' .format(number_1, number_2, (number_1 / number_2)))